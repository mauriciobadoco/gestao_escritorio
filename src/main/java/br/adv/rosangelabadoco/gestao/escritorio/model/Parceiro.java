package br.adv.rosangelabadoco.gestao.escritorio.model;

/**
 *
 * @author mauri
 */
public class Parceiro {
    private Long id;
    private String nome;
   
    public Parceiro() {
    }

    public Parceiro(Long id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
}
